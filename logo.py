import cairo

surface = cairo.ImageSurface(cairo.FORMAT_ARGB32, 120, 80)
ctx = cairo.Context(surface)

ctx.set_source_rgb(1, 1, 1)
ctx.paint()

ctx.move_to(10, 10)
ctx.line_to(110, 10)
ctx.line_to(110, 20)
ctx.line_to(90, 20)
ctx.line_to(90, 70)
ctx.line_to(80, 70)
ctx.line_to(80, 20)
ctx.line_to(40, 20)
ctx.line_to(40, 70)
ctx.line_to(30, 70)
ctx.line_to(30, 20)
ctx.line_to(10, 20)
ctx.line_to(10, 10)
ctx.set_source_rgb(0.75, 0, 0)
ctx.fill()

ctx.move_to(40, 20)
ctx.line_to(50, 20)
ctx.line_to(50, 40)
ctx.line_to(70, 20)
ctx.line_to(80, 20)
ctx.line_to(80, 30)
ctx.line_to(72.5, 30)
ctx.line_to(62.5, 40)
ctx.line_to(75, 40)
ctx.line_to(75, 50)
ctx.line_to(62.5, 50)
ctx.line_to(72.5, 60)
ctx.line_to(80, 60)
ctx.line_to(80, 70)
ctx.line_to(70, 70)
ctx.line_to(50, 50)
ctx.line_to(50, 70)
ctx.line_to(40, 70)
ctx.line_to(40, 20)
ctx.set_source_rgb(0, 0.75, 0)
ctx.fill()

surface.write_to_png("logo.png")
